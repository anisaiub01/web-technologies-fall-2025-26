            <p>
             Khamarbari began as a humble concept to empower farmers in rural areas and to provide them with a channel to reach the consumers. The founders observed that middlemen were denying many farmers a fair price of their produce and the consumers were paying more money on low-quality products. This gap led to the development of an open, friendly system where farmers could sell their products directly and consumers could get access to fresh and local products.

We are driven by the urge to build a sustainable agricultural ecosystem. Khamarbari not only focuses on bettering the income of the farmers, but also trains them on the current farming methods, seasonal farms, and effective logistics. We feel that technology can revolutionize agriculture and enable rural communities to prosper in the economic arena.

Khamarbari expanded to serve several villages since its establishment, linking dozens of farmers to hundreds of consumers. Our platform makes the supply chain easier, provides reasonable prices, and fosters trust between manufacturers and consumers. Khamarbari is building the future of local agriculture by creating a community where transparency, efficiency, and sustainability are cherished.

Our vision is to connect the rural and urban communities, decrease food waste, and advance organic and fresh fruits and vegetables. The world that we imagine is a place where farmers receive fair wages, consumers receive healthy food and technology is positively impacting society.
